@router.post(
    "/batch",
    response_model=BatchResponse,
    status_code=status.HTTP_202_ACCEPTED,
    summary="Submit batch processing job",
    description="Submit multiple video URLs for batch processing. Each video becomes a separate project.",
)
async def batch_process(
    body: BatchProcessRequest,
    auth: Dict[str, Any] = Depends(authenticate_api_key),
):
    """Batch processing: submit multiple videos at once."""
    if "projects:write" not in auth["scopes"]:
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail={
                "error": "insufficient_scope",
                "message": "API key does not have 'projects:write' permission.",
                "request_id": str(uuid.uuid4()),
            },
        )

    # Validate all videos have a URL
    for i, video in enumerate(body.videos):
        if "url" not in video:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail={
                    "error": "invalid_video_entry",
                    "message": f"Video at index {i} is missing required 'url' field.",
                    "request_id": str(uuid.uuid4()),
                },
            )

    credits_required = len(body.videos)
    batch_id = str(uuid.uuid4())
    project_ids = []
    now = datetime.now(timezone.utc).isoformat()

    # Create individual projects for each video
    for video in body.videos:
        project_id = str(uuid.uuid4())
        config = {**(body.default_config or {}), **(video.get("config", {}))}

        project = {
            "id": project_id,
            "user_id": auth["user_id"],
            "title": video.get("title", f"Batch {batch_id[:8]} - Video"),
            "video_url": video["url"],
            "config": config,
            "status": "queued",
            "created_at": now,
            "updated_at": now,
            "clips_count": 0,
            "error": None,
            "is_sandbox": auth["is_sandbox"],
            "batch_id": batch_id,
        }
        _projects_store[project_id] = project
        project_ids.append(project_id)

    return BatchResponse(
        batch_id=batch_id,
        project_ids=project_ids,
        total_videos=len(body.videos),
        credits_required=credits_required,
        status="queued",
        webhook_url=body.webhook_url,
    )
