async def send_webhook(url: str, event: str, payload: Dict[str, Any]) -> bool:
    """
    Task 18.4: Send a webhook notification to a registered URL.

    Includes HMAC-SHA256 signature in X-Webhook-Signature header if a
    secret is configured for the webhook.

    Args:
        url: The webhook delivery URL.
        event: Event type (project.completed, project.failed, credits.low).
        payload: Event payload data.

    Returns:
        True if delivered successfully, False otherwise.
    """
    webhook_payload = {
        "event": event,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "data": payload,
        "api_version": API_VERSION,
    }

    # Check if this is a sandbox webhook
    if payload.get("sandbox"):
        webhook_payload["sandbox"] = True

    # Find webhook secret for HMAC signing
    webhook_secret = None
    for wh in _webhooks_store.values():
        if wh["url"] == url:
            webhook_secret = wh.get("secret")
            break

    headers = {
        "Content-Type": "application/json",
        "User-Agent": "ShortsEngine-Webhooks/1.0",
        "X-Webhook-Event": event,
        "X-Webhook-ID": str(uuid.uuid4()),
    }

    # Sign payload if secret is configured
    if webhook_secret:
        import json
        payload_bytes = json.dumps(webhook_payload, sort_keys=True).encode()
        signature = hmac.new(
            webhook_secret.encode(), payload_bytes, hashlib.sha256
        ).hexdigest()
        headers["X-Webhook-Signature"] = f"sha256={signature}"

    try:
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.post(url, json=webhook_payload, headers=headers)
            return response.status_code < 400
    except Exception:
        # Log failure, schedule retry (in production, use exponential backoff)
        return False



