def _hash_api_key(key: str) -> str:
    """Hash an API key for secure storage."""
    return hashlib.sha256(key.encode()).hexdigest()


def _mask_api_key(key: str) -> str:
    """Mask an API key for display (show prefix + last 6 chars)."""
    if key.startswith(SANDBOX_KEY_PREFIX):
        return f"test_...{key[-6:]}"
    return f"sk_...{key[-6:]}"


def _is_sandbox_key(key: str) -> bool:
    """Check if an API key is a sandbox/test key (Task 18.8)."""
    return key.startswith(SANDBOX_KEY_PREFIX)


def _generate_api_key(sandbox: bool = False) -> str:
    """Generate a new API key with appropriate prefix."""
    prefix = SANDBOX_KEY_PREFIX if sandbox else PRODUCTION_KEY_PREFIX
    random_part = secrets.token_urlsafe(32)
    return f"{prefix}{random_part}"


def _check_rate_limit(key_id: str, plan: str) -> bool:
    """
    Task 18.3: Check if the API key has exceeded its rate limit.
    Returns True if request is allowed, False if rate-limited.
    """
    limit = RATE_LIMITS.get(plan, RATE_LIMITS["free"])
    now = time.time()
    window_start = now - 60  # 1-minute sliding window

    if key_id not in _rate_limit_tracker:
        _rate_limit_tracker[key_id] = []

    # Remove timestamps outside the window
    _rate_limit_tracker[key_id] = [
        ts for ts in _rate_limit_tracker[key_id] if ts > window_start
    ]

    if len(_rate_limit_tracker[key_id]) >= limit:
        return False

    _rate_limit_tracker[key_id].append(now)
    return True



async def authenticate_api_key(x_api_key: Optional[str] = Header(None)) -> Dict[str, Any]:
    """
    Dependency: Authenticate request via X-API-Key header.
    Returns the user context associated with the key.
    """
    if not x_api_key:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail={
                "error": "authentication_required",
                "message": "Missing X-API-Key header. Provide a valid API key.",
                "request_id": str(uuid.uuid4()),
            },
        )

    key_hash = _hash_api_key(x_api_key)
    key_record = None

    for kid, record in _api_keys_store.items():
        if record["key_hash"] == key_hash and record["is_active"]:
            key_record = record
            key_record["key_id"] = kid
            break

    if not key_record:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail={
                "error": "invalid_api_key",
                "message": "The provided API key is invalid or has been revoked.",
                "request_id": str(uuid.uuid4()),
            },
        )

    # Task 18.3: Check rate limit
    plan = key_record.get("plan", "free")
    if not _check_rate_limit(key_record["key_id"], plan):
        limit = RATE_LIMITS.get(plan, RATE_LIMITS["free"])
        raise HTTPException(
            status_code=status.HTTP_429_TOO_MANY_REQUESTS,
            detail={
                "error": "rate_limit_exceeded",
                "message": f"Rate limit exceeded. Plan '{plan}' allows {limit} requests/minute.",
                "retry_after_seconds": 60,
                "request_id": str(uuid.uuid4()),
            },
            headers={"Retry-After": "60"},
        )

    # Update last used timestamp
    key_record["last_used_at"] = datetime.now(timezone.utc).isoformat()

    return {
        "user_id": key_record["user_id"],
        "key_id": key_record["key_id"],
        "plan": plan,
        "scopes": key_record.get("scopes", []),
        "is_sandbox": _is_sandbox_key(x_api_key),
    }



