@router.post(
    "/keys",
    response_model=APIKeyResponse,
    status_code=status.HTTP_201_CREATED,
    summary="Generate a new API key",
    description="Generate a new API key. The key value is returned ONLY in this response — store it securely.",
)
async def create_api_key(
    body: APIKeyCreateRequest,
    auth: Dict[str, Any] = Depends(authenticate_api_key),
):
    """Generate a new API key (returned only once)."""
    # Check key limit (max 10 active keys per user)
    user_keys = [
        k for k in _api_keys_store.values()
        if k["user_id"] == auth["user_id"] and k["is_active"]
    ]
    if len(user_keys) >= 10:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail={
                "error": "key_limit_reached",
                "message": "Maximum of 10 active API keys per user. Revoke an existing key first.",
                "request_id": str(uuid.uuid4()),
            },
        )

    # Generate key
    is_sandbox = auth["is_sandbox"]
    raw_key = _generate_api_key(sandbox=is_sandbox)
    key_id = str(uuid.uuid4())
    now = datetime.now(timezone.utc).isoformat()

    # Store only the hash
    _api_keys_store[key_id] = {
        "key_hash": _hash_api_key(raw_key),
        "user_id": auth["user_id"],
        "name": body.name,
        "scopes": body.scopes,
        "plan": auth["plan"],
        "is_active": True,
        "is_sandbox": is_sandbox,
        "created_at": now,
        "last_used_at": None,
    }

    return APIKeyResponse(
        key_id=key_id,
        key=raw_key,
        name=body.name,
        scopes=body.scopes,
        created_at=now,
        is_sandbox=is_sandbox,
    )



